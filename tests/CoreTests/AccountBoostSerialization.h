#pragma once

#include "QuidCore/Account.h"
#include "QuidBoostSerialization.h"

//namespace Quid {
namespace boost
{
  namespace serialization
  {
    template <class Archive>
    inline void serialize(Archive &a, Quid::AccountKeys &x, const boost::serialization::version_type ver)
    {
      a & x.address;
      a & x.spendSecretKey;
      a & x.viewSecretKey;
    }

    template <class Archive>
    inline void serialize(Archive &a, Quid::AccountPublicAddress &x, const boost::serialization::version_type ver)
    {
      a & x.spendPublicKey;
      a & x.viewPublicKey;
    }

  }
}
